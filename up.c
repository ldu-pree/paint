void	up()
{
	int i;
	int j;

	i = 0;
	j = 0 - pix;
	oy = oy - pix;
	while (i <= pix)
	{
		j = 0 - pix;
		while (j <= 0)
		{
			mlx_pixel_put(mlx, win, ox - i + pix, oy - j, 0x00000000);
		j++;
		}
		i++;
	}
	//oy = oy - j + 1;
}
